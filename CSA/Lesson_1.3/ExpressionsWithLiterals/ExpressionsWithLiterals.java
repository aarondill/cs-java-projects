public class ExpressionsWithLiterals {
  public static void main(String... args) {
    System.out.println(3 - 14 + 8);
    System.out.println(3 - (14 + 18));
    System.out.println(3 - 14.0 + 8);
    System.out.println(9 / 2 * 5 + 6);
    System.out.println(9.0 / 2.0 * 5 + 6);
    System.out.println(9 / (2 * 5) + 6);
    System.out.println(9 / (2.0 * 5) + 6);
    System.out.println(17 % 3);
    System.out.println(43 - 25 % 11 * 8);
    System.out.println(8 + 10 / 20);
  }
}
