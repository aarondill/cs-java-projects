class NumberPrefix {
    public static void main(String[] args) {
        System.out.println("Binary: ");
        System.out.println("0b0100: " + 0b0100);
        System.out.println();

        System.out.println("Hex:");
        System.out.println("0x0100: " + 0x0100);
        System.out.println();

        System.out.println("Decimal:");
        System.out.println("100: " + 100);
        System.out.println();

        System.out.println("Octal:");
        System.out.println("0100: " + 0100);
    }
}
